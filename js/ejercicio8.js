/*Ciclo for y suma: Escribe un programa que sume todos los números del 1 al
100 usando un ciclo for .*/

let suma = 0; // Inicializa la variable suma en 0

for (let i = 1; i <= 100; i++) {
    suma += i; // Suma i a la variable suma
}

console.log("La suma de los números del 1 al 100 es:", suma); // Imprime el resultado
